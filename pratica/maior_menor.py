numero_1 = int(input('digite um numero'))
numero_2 = int(input('digite um numero'))

if numero_1 > numero_2  :
    print(f'número {numero_1} é maior que o número {numero_2}')
elif numero_1 < numero_2 :
    print(f'número {numero_1} é menor que o número {numero_2}')
else :
    print(f'os número {numero_1} e {numero_2} são iguais')
